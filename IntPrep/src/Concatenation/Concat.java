package Concatenation;

public class Concat {
    public static void main(String[] args) {
        System.out.println(10 + 50 + "Henry");
        System.out.println("Bob" + 10 + 50);
        System.out.println("Carl" + (10+50));

        System.out.println(10 * 50 + "Canban");
        System.out.println("Alicia" + 10 * 50);
        System.out.println("Sam" + (10*50));
    }
}
